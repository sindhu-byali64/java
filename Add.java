package JAVA;

public class Add {
        public static void main(String[] args) { // "static" should be lowercase
            int a = 10;
            int b = 20;
            int sum= a + b;
            int sub= a - b;
            System.out.println("sum of a & b is: " + sum);
            System.out.println("subtraction of a & b is: " + sub);
        }
}
